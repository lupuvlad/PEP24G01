# Get 2 numbers from the user
num1 = int(input("num1 = "))
num2 = int(input("num2 = "))

# Calculate the average of the num1 and num2
average = (num1 + num2) / 2
print("Average =", average)

# Integer division
int_division = num1 // num2
print("Integer division =", int_division)

# num1 ** num2
power = num1 ** num2
print("num1 ** num2 =", power)
