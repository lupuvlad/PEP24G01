int1 = 100
int2 = 200

result = int1 + int2
print(result)

new_result = result - int1
print(new_result)

# Square root
sqrt = int1 ** (1/2)
print(sqrt)

# Float
add1 = 10.1 + 10.2
print(add1)

# Set a method directly to an int number
add_result = (10).__add__(10)
print(add_result)

# Modulo
modulo_result = 10 % 3
print(modulo_result)

# Integer division
result = 10 // 3
print(result)
