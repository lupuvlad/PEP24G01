# Print the strings

# Separated by _
print("Hello", "Python", sep="_")
print("Ana", "are", "mere", sep="_")
print("Pizza", "Party", sep="_")

# With . at the end of each string
print("Hello", "Python", end=".")
print("Ana", "are", "mere", end=".")
print("Pizza", "Party", end=".")

# Blank line
print("")

# First word multiplied by 4
print("Hello" * 4, "Python")
print("Ana" * 4, "are", "mere")
print("Pizza" * 4, "Party")
